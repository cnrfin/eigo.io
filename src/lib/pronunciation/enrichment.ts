/** Sound-guide hub pages (e.g. /pronunciation/sounds/r-l). These target the
 *  high-volume "sound" queries (「LとRの違い」「L R 発音」) that the per-word pages
 *  can't, and link down to every word in the category. `key` matches the base
 *  category key; a guide is published when its category has published words. */
export type SoundSide = { label: string; symbol: string; short: string; steps: string[]; keyJa: string }
export type SoundGuide = {
  key: string
  slug: string
  titleJa: string
  metaTitle: string
  metaDesc: string
  keywords: string
  introJa: string
  /** Consonant or vowel — groups the guides in the index browser. */
  group: 'consonant' | 'vowel'
  /** Compact label for the mobile sound strip, where full titles don't fit. */
  shortLabel: string
  /** One line for the index hero card. Shorter and more actionable than introJa. */
  taglineJa: string
  // Common words carrying the sound that have no minimal-pair partner. They are
  // listed under この音を含む単語 and get their own word pages, but never enter
  // the trainer's pairs.
  extraWords?: string[]
  // Interactive trainer config: the two contrasting sides, the headline pair,
  // and which slugs belong to the first side (for role tagging / colouring).
  // Voices are derived from what every word in the guide actually has, so there
  // is no per-guide voice config.
  // `pairs` (optional) lets a guide declare its own pairs and membership, in
  // [r-side, l-side] order. Required when a contrast spans two lexical sets, or
  // when a word belongs to more than one guide with a different partner in each.
  // Without it, membership comes from `key` and pairing from each word's `pair`.
  trainer: {
    sides: { r: SoundSide; l: SoundSide }
    featured: [string, string]
    rSlugs: string[]
    pairs?: [string, string][]
  }
  faqs: { q: string; a: string }[]
}

